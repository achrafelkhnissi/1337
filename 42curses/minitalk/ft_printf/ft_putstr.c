#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	len;

	len = 0;
	if (s == NULL)
		return (ft_putstr("(null)"));
	while (*s)
	{
		write(1, s++, 1);
		len++;
	}
	return (len);
}
